export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations.en

// Language detection utility
export function detectLanguage(): Language {
  if (typeof window === 'undefined') return 'en'

  const browserLang = navigator.language.toLowerCase()
  const savedLang = localStorage.getItem('preferred-language') as Language

  if (savedLang && savedLang in translations) {
    return savedLang
  }

  if (browserLang.includes('hi')) return 'hi'
  if (browserLang.includes('ta')) return 'ta'
  if (browserLang.includes('bn')) return 'bn'

  return 'en'
}

// Save language preference
export function saveLanguagePreference(language: Language) {
  if (typeof window !== 'undefined') {
    localStorage.setItem('preferred-language', language)
  }
}

// Get translation function
export function getTranslation(language: Language, key: TranslationKey): string {
  const value = translations[language]?.[key] || translations.en[key]
  if (typeof value === 'string') {
    return value
  }
  return key as string
}

// React hook for translations
export function useTranslations(defaultLanguage?: Language) {
  const [language, setLanguage] = React.useState<Language>(defaultLanguage || detectLanguage())

  const t = React.useCallback(
    (key: TranslationKey) => {
      return getTranslation(language, key)
    },
    [language]
  )

  const changeLanguage = React.useCallback((newLanguage: Language) => {
    setLanguage(newLanguage)
    saveLanguagePreference(newLanguage)
  }, [])

  return {
    language,
    t,
    changeLanguage,
    availableLanguages: Object.keys(translations) as Language[],
  }
}

