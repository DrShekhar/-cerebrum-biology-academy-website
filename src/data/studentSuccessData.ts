// Function to get random success story
export function getRandomSuccessStory(): {
  message: string
  timestamp: Date
  student: StudentSuccess
} {
  const student = studentSuccessDatabase[Math.floor(Math.random() * studentSuccessDatabase.length)]
  const template = successTemplates[Math.floor(Math.random() * successTemplates.length)]

  let message = template
    .replace('{name}', student.name)
    .replace('{location}', student.location)
    .replace('{college}', student.college)
    .replace('{course}', student.course)

  if (student.score && message.includes('{score}')) {
    message = message.replace('{score}', student.score.toString())
  }

  if (student.rank && message.includes('{rank}')) {
    message = message.replace('{rank}', student.rank.toString())
  }

  // Remove unused placeholders
  message = message
    .replace(/\{[^}]+\}/g, '')
    .replace(/\s+/g, ' ')
    .trim()

  return {
    message,
    timestamp: new Date(),
    student,
  }
}

