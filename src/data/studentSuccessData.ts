// Generate realistic success stories
function generateStudentName(): string {
  const firstName = indianFirstNames[Math.floor(Math.random() * indianFirstNames.length)]
  const lastName = indianLastNames[Math.floor(Math.random() * indianLastNames.length)]
  return `${firstName} ${lastName}`
}

function generateLocation(): string {
  return indianLocations[Math.floor(Math.random() * indianLocations.length)]
}

function generateCollege(): string {
  return medicalColleges[Math.floor(Math.random() * medicalColleges.length)]
}

function generateScore(): number {
  // NEET scores typically range from 400-720, focusing on higher scores for success stories
  return Math.floor(Math.random() * 200) + 520 // 520-720 range
}

function generateRank(): number {
  // AIR ranks for successful candidates
  return Math.floor(Math.random() * 50000) + 1 // 1-50000 range
}

