// Helper functions for units
export function getUnitBySlug(slug: string): CampbellBiologyUnit | undefined {
  return campbellUnits.find((unit) => unit.slug === slug)
}

export function getUnitById(id: string): CampbellBiologyUnit | undefined {
  return campbellUnits.find((unit) => unit.id === id)
}

export function getUnitByNumber(unitNumber: number): CampbellBiologyUnit | undefined {
  return campbellUnits.find((unit) => unit.unitNumber === unitNumber)
}

export function getAdjacentUnits(unitNumber: number): {
  previous: CampbellBiologyUnit | null
  next: CampbellBiologyUnit | null
} {
  const currentIndex = campbellUnits.findIndex((u) => u.unitNumber === unitNumber)

  return {
    previous: currentIndex > 0 ? campbellUnits[currentIndex - 1] : null,
    next: currentIndex < campbellUnits.length - 1 ? campbellUnits[currentIndex + 1] : null,
  }
}

// Get total estimated hours across all units
export function getTotalEstimatedHours(): number {
  return campbellUnits.reduce((total, unit) => total + unit.estimatedHours, 0)
}

// Get high olympiad relevance units (4+ rating)
export function getHighOlympiadUnits(): CampbellBiologyUnit[] {
  return campbellUnits.filter((unit) => unit.olympiadRelevance >= 4)
}
