// Read existing colleges
const collegesPath = path.join(__dirname, '../src/data/colleges.json')
const colleges = JSON.parse(fs.readFileSync(collegesPath, 'utf8'))
const existingNames = new Set(colleges.map((c) => c.name.toLowerCase()))

// Helper to check if college exists
function exists(name) {
  return existingNames.has(name.toLowerCase())
}

// Helper to generate cutoffs based on tier
function generateCutoffs(tier, type) {
  const baseRank = {
    1: { general: 500, ews: 900, obc: 1600, sc: 4000, st: 9000 },
    2: { general: 5000, ews: 8000, obc: 12000, sc: 25000, st: 45000 },
    3: { general: 25000, ews: 35000, obc: 50000, sc: 80000, st: 120000 },
    4: { general: 60000, ews: 80000, obc: 100000, sc: 150000, st: 200000 },
    5: { general: 100000, ews: 120000, obc: 150000, sc: 200000, st: 300000 },
  }

  const base = baseRank[tier] || baseRank[3]
  const variance = 0.2

  return {
    general: Math.round(base.general * (1 + (Math.random() - 0.5) * variance)),
    ews: Math.round(base.ews * (1 + (Math.random() - 0.5) * variance)),
    obc: Math.round(base.obc * (1 + (Math.random() - 0.5) * variance)),
    sc: Math.round(base.sc * (1 + (Math.random() - 0.5) * variance)),
    st: Math.round(base.st * (1 + (Math.random() - 0.5) * variance)),
  }
}

