// Format fee display
function formatFee(fees) {
  if (fees < 1000) return `₹${fees}/year`
  if (fees < 100000) return `₹${(fees / 1000).toFixed(0)}K/year`
  return `₹${(fees / 100000).toFixed(1)}L/year`
}

// Add new colleges
let addedCount = 0
const skipped = []

newColleges.forEach((college) => {
  if (exists(college.name)) {
    skipped.push(college.name)
    return
  }

  const cutoffs = generateCutoffs(college.tier, college.type)
  const isAIQOnly = college.quotaType === 'AIQ_Only'
  const aiqSeats = isAIQOnly ? college.seats : Math.round(college.seats * 0.15)
  const stateSeats = isAIQOnly ? 0 : college.seats - aiqSeats

  const newCollege = {
    name: college.name,
    state: college.state,
    type: college.type === 'Government' ? 'Government' : 'Private/Deemed',
    quotaType: college.quotaType,
    tier: college.tier,
    totalSeats: college.seats,
    aiqSeats: aiqSeats,
    stateSeats: stateSeats,
    nirfRank: null,
    aiqCutoffs: cutoffs,
    stateCutoffs: isAIQOnly
      ? null
      : {
          general: Math.round(cutoffs.general * 1.3),
          ews: Math.round(cutoffs.ews * 1.3),
          obc: Math.round(cutoffs.obc * 1.3),
          sc: Math.round(cutoffs.sc * 1.3),
          st: Math.round(cutoffs.st * 1.3),
        },
    fees: college.fees,
    feeDisplay: formatFee(college.fees),
  }

  colleges.push(newCollege)
  addedCount++
})

// Sort colleges by tier and name
colleges.sort((a, b) => {
  if (a.tier !== b.tier) return a.tier - b.tier
  return a.name.localeCompare(b.name)
})

// Save updated file
fs.writeFileSync(collegesPath, JSON.stringify(colleges, null, 2))

